#!/usr/bin/env node

/**
 * add-max-energy.js — Compute max_photon_energy_keV from ICRP 107 photon data
 *
 * Reads nuclides.json + icrp107-index.json, calculates the maximum photon energy
 * from the filtered photon list for each nuclide, and adds/updates max_photon_energy_keV.
 *
 * Then regenerates nuclides-data.js for offline support.
 *
 * Usage:
 *   node tools/add-max-energy.js
 */

const fs = require('fs');
const path = require('path');

const nuclideJsonPath = path.join(__dirname, '..', 'data', 'nuclides.json');
const icrpJsonPath    = path.join(__dirname, '..', 'data', 'icrp107-index.json');

try {
  console.log('Loading nuclides.json and icrp107-index.json...');
  const nuclideData = JSON.parse(fs.readFileSync(nuclideJsonPath, 'utf8'));
  const icrpData    = JSON.parse(fs.readFileSync(icrpJsonPath, 'utf8'));

  // Build map for fast lookup
  const icrpMap = {};
  for (const n of icrpData.nuclides) {
    icrpMap[n.id] = n;
  }

  // Add max_photon_energy_keV to each nuclide
  console.log(`\nComputing max photon energy for ${nuclideData.nuclides.length} nuclides...`);
  for (const nuclide of nuclideData.nuclides) {
    const icrp = icrpMap[nuclide.id];
    if (!icrp || !icrp.photons || icrp.photons.length === 0) {
      nuclide.max_photon_energy_keV = null;
    } else {
      nuclide.max_photon_energy_keV = Math.max(...icrp.photons.map(p => p.energy_keV));
    }
  }

  // Write updated nuclides.json
  console.log(`Writing ${nuclideJsonPath}...`);
  fs.writeFileSync(nuclideJsonPath, JSON.stringify(nuclideData, null, 2) + '\n', 'utf8');

  // Regenerate nuclides-data.js
  const jsPath = path.join(__dirname, '..', 'data', 'nuclides-data.js');
  console.log(`Writing ${jsPath}...`);
  fs.writeFileSync(jsPath,
    `// Generated by tools/generate-data.js. Do not edit manually.\nconst NUCLIDE_DATA = ${JSON.stringify(nuclideData, null, 2)};\n`,
    'utf8');

  console.log(`\n✓ Done! max_photon_energy_keV added and nuclides-data.js regenerated.\n`);
  console.log('Summary by nuclide:');
  for (const n of nuclideData.nuclides) {
    console.log(`  ${n.id}: max = ${n.max_photon_energy_keV ?? 'null'} keV`);
  }
} catch (err) {
  console.error(`✗ Error: ${err.message}`);
  process.exit(1);
}
